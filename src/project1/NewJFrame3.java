    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int i = jTable1.getSelectedRow();
        DefaultTableModel model02 = (DefaultTableModel)jTable1.getModel();
        if(i >= 0)
        {
           String owner = (String) model02.getValueAt(i, 0);
           String item = (String) model02.getValueAt(i, 1);
           String price = (String) model02.getValueAt(i, 2);
           try {
            dataOutputStream.writeInt(4);
            dataOutputStream.writeUTF(owner);
            dataOutputStream.writeUTF(item);
            dataOutputStream.writeUTF(price);
            dataOutputStream.writeUTF(userName);
            
            int flag = dataInputStream.readInt();
            if (flag == 1) {
                this.setVisible(false);
                new NewJFrame2(userName,item,price).setVisible(true);
            } else {
                this.setVisible(false);
              
                new NewJFrame5(userName).setVisible(true);
            }

        } catch (Exception err) {
            System.out.println("error in buy button");
        }
        }else {
        JOptionPane.showMessageDialog(null, "Choose the Item you want to Buy!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

