    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        LinkedList<String> items = new LinkedList<>();
        LinkedList<String> price = new LinkedList<>();
        for(int i = 0; i < model.getRowCount(); i++){
            items.add(model.getValueAt(i, 0).toString());
            price.add(model.getValueAt(i, 1).toString());                                  
        }
        
        try {
            dataOutputStream.writeInt(3);
            dataOutputStream.writeUTF(userName);
            dataOutputStream.writeInt(items.size());
            for(int i=0;i<items.size();i++)
            {
               dataOutputStream.writeUTF(items.get(i));
               dataOutputStream.writeUTF(price.get(i));
            }
            
            int flag = dataInputStream.readInt();
            if (flag == 1) {
                this.setVisible(false);
                new NewJFrame3(userName).setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "ERROR 6");
            }

        } catch (Exception err) {
            System.out.println("error1");
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

